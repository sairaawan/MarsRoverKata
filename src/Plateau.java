public class Plateau {
    private final int X_MAX;
    private final int Y_MAX;

    public Plateau(int x_max, int y_max) {
        this.X_MAX = x_max;
        this.Y_MAX = y_max;

    }

    public int getX_MAX() {
        return X_MAX;
    }

    public int getY_MAX() {
        return Y_MAX;
    }

}
